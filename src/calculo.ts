let cant: number

export const getCant = (number: number) => cant = number

const calculoPesado = () => {
  for (let i = 0; i <= cant; i++) {
    const random = Math.round(Math.random()*(1000 - 1) + 1)
    const array: number[] = []
    array.push(random)
    return array
  }
  return false
}

export default calculoPesado